/*
Escrever um loop for ou while que exibe qual número é par e qual é impar
de 0 a 50
*/

for(let parOuImpar = 0; parOuImpar <= 50; parOuImpar++) {

      // verificar se é PAR ou IMPAR

      if (parOuImpar % 2 == 0) {
            console.log(`O número ${parOuImpar} é Par!`)
      } else {
            console.log(`O número ${parOuImpar} é Impar!`)
      }
}
