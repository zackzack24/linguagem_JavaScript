// Escreva uma função que receba dois números, o primeiro é a base e o segundo é o expoente
// Depois retornar o resultado

function exponenciacao(base, expoente) {
      return Math.pow(base, expoente);
}

console.log(exponenciacao(1,3));
console.log(exponenciacao(174,3));
console.log(exponenciacao(531,63));
